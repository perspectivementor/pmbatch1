public class CharExample {

    public static void main(String[] args) {

        // Declare and initialize a char variable
        char myChar = 'A';

        // Print the char variable
        System.out.println("The value of myChar is: " + myChar);

        // You can also perform some arithmetic operations with char
        char nextChar = (char) (myChar + 1);
        System.out.println("The next character is: " + nextChar);


    }
}
